# In Ruby, a ! is a "not" operator and a ? indicates an expression is being
# checked for truthiness. But if they're at the end of a method, it's a part
# of the method name.

# 1. != means "is not equal to". It is used in an equality comparison
# 2. ! before something means "not something"
# 3. ! after something means it is a mutating method and will mutate the object
# 4. ? before something is a ternary operator
# 5. ? after something will check for truthiness
# 6. !! before something will make it the thing